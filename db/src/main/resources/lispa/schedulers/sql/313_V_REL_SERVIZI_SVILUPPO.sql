CREATE OR REPLACE FORCE VIEW V_REL_SERVIZI_SVILUPPO
(
   FK_WI_SERVIZI,
   TIPO_WI_SERVIZI,
   FK_WI_SVILUPPO,
   TIPO_WI_SVILUPPO
)
AS
(select lw.FK_WI_PADRE, lw.TIPO_WI_PADRE, lw.FK_WI_FIGLIO, lw.TIPO_WI_FIGLIO
 from DMALM_LINKED_WORKITEMS lw
 join DMALM_TEMPLATE_SERVIZI tse on lw.FK_WI_PADRE = tse.fk_wi
 join DMALM_TEMPLATE_SVILUPPO tsv on lw.FK_WI_FIGLIO = tsv.fk_wi
 union
 select lw.FK_WI_FIGLIO, lw.TIPO_WI_FIGLIO, lw.FK_WI_PADRE, lw.TIPO_WI_PADRE
 from DMALM_LINKED_WORKITEMS lw
 join DMALM_TEMPLATE_SERVIZI tse on lw.FK_WI_FIGLIO = tse.fk_wi
 join DMALM_TEMPLATE_SVILUPPO tsv on lw.FK_WI_PADRE = tsv.fk_wi);
