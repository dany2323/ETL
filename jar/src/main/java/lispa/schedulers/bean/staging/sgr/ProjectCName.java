package lispa.schedulers.bean.staging.sgr;

public class ProjectCName {

    private String cName;

    private int primaGraffaIndex;
    
    private int secondaGraffaIndex;
    
    private String identificativo;
    
    private String descrizione;
    
    private String prodotto;
    
    private String modulo;
    
    private String funzionalita;
    
    

	public String getDescrizione() {
		return descrizione;
	}

	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}

	public String getProdotto() {
		return prodotto;
	}

	public void setProdotto(String prodotto) {
		this.prodotto = prodotto;
	}

	public String getModulo() {
		return modulo;
	}

	public void setModulo(String modulo) {
		this.modulo = modulo;
	}

	public String getFunzionalita() {
		return funzionalita;
	}

	public void setFunzionalita(String funzionalita) {
		this.funzionalita = funzionalita;
	}

	public String getcName() {
		return cName;
	}

	public void setcName(String cName) {
		this.cName = cName;
	}

	public int getPrimaGraffaIndex() {
		return primaGraffaIndex;
	}

	public void setPrimaGraffaIndex(int primaGraffaIndex) {
		this.primaGraffaIndex = primaGraffaIndex;
	}

	public int getSecondaGraffaIndex() {
		return secondaGraffaIndex;
	}

	public void setSecondaGraffaIndex(int secondaGraffaIndex) {
		this.secondaGraffaIndex = secondaGraffaIndex;
	}

	public String getIdentificativo() {
		return identificativo;
	}

	public void setIdentificativo(String identificativo) {
		this.identificativo = identificativo;
	}
    

}

